    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Tombol "Kirim" â†’ INSERT laporan baru
        String tanggal = jFormattedTextField3.getText().trim();
        String jenis = jTextField9.getText().trim();
        String deskripsi = jTextField7.getText().trim();

        // âœ… Status default
        String status = "Menunggu Verifikasi Admin";

        if (tanggal.isEmpty() || jenis.isEmpty() || deskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tanggal, Jenis Kendala, dan Deskripsi wajib diisi!",
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = db_connection.getConnection()) {
            // Ambil proyek kontraktor
            String getProyekId = "SELECT Proyek_id_proyek FROM pengguna WHERE username = ?";
            PreparedStatement stmt1 = conn.prepareStatement(getProyekId);
            stmt1.setString(1, loggedInUsername);
            ResultSet rs = stmt1.executeQuery();

            if (rs.next()) {
                String proyekId = rs.getString("Proyek_id_proyek");

                // âœ… Generate ID laporan baru format 5 digit (contoh: L0001, L0012)
                String getLastId = "SELECT id_laporan FROM laporan ORDER BY id_laporan DESC LIMIT 1";
                Statement stmt2 = conn.createStatement();
                ResultSet rs2 = stmt2.executeQuery(getLastId);
                String newId = "L0001";
                if (rs2.next()) {
                    String lastId = rs2.getString("id_laporan");
                    int num = Integer.parseInt(lastId.substring(1)) + 1;
                    newId = String.format("L%04d", num); // ðŸ‘ˆ 4 angka setelah huruf L
                }

                // âœ… Insert laporan baru
                String insert = """
                    INSERT INTO laporan (id_laporan, tanggal_laporan, jenis_kendala, deskripsi, status_tindak_lanjut, Proyek_id_proyek)
                    VALUES (?, ?, ?, ?, ?, ?);
                """;
                PreparedStatement stmtInsert = conn.prepareStatement(insert);
                stmtInsert.setString(1, newId);
                stmtInsert.setDate(2, Date.valueOf(tanggal));
                stmtInsert.setString(3, jenis);
                stmtInsert.setString(4, deskripsi);
                stmtInsert.setString(5, status);
                stmtInsert.setString(6, proyekId);

                int rows = stmtInsert.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this,
                        "âœ… Laporan berhasil dikirim.\nStatus awal otomatis: " + status + ".",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

                    // âœ… Setelah berhasil tambah laporan â†’ buka Menampilkan_Laporan dan highlight ID baru
                    Menampilkan_Laporan tampilFrame = new Menampilkan_Laporan(loggedInUsername, newId);
                    tampilFrame.setLocationRelativeTo(null);
                    tampilFrame.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menambah laporan.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Proyek kontraktor tidak ditemukan!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Kesalahan database: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                "Format tanggal harus yyyy-mm-dd!",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

