    // ✅ Fungsi untuk load data pengeluaran dari database
    private void loadExpenseData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Pengeluaran");
        model.addColumn("Tanggal Pengeluaran");
        model.addColumn("Keterangan");
        model.addColumn("Jumlah (Rp)");
        model.addColumn("Kategori");

        try (Connection conn = db_connection.getConnection()) {
            String query = """
                SELECT pg.id_pengeluaran, pg.tanggal_pengeluaran, pg.keterangan, pg.jumlah, pg.kategori
                FROM pengeluaran pg
                INNER JOIN proyek pr ON pg.Proyek_id_proyek = pr.id_proyek
                INNER JOIN pengguna p ON pr.id_proyek = p.Proyek_id_proyek
                WHERE p.username = ?;
            """;

            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, loggedInUsername);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                // ✅ Format jumlah ke Rupiah
                double jumlah = rs.getDouble("jumlah");
                String formattedJumlah = formatRupiah(jumlah);

                model.addRow(new Object[]{
                    rs.getString("id_pengeluaran"),
                    rs.getDate("tanggal_pengeluaran"),
                    rs.getString("keterangan"),
                    formattedJumlah,
                    rs.getString("kategori")
                });
            }

            // ✅ Update tampilan tabel di UI
            SwingUtilities.invokeLater(() -> jTable1.setModel(model));

            if (model.getRowCount() == 0) {
                SwingUtilities.invokeLater(() ->
                    JOptionPane.showMessageDialog(this, "Tidak ada data pengeluaran ditemukan.", "Informasi", JOptionPane.INFORMATION_MESSAGE)
                );
            }

        } catch (SQLException ex) {
            SwingUtilities.invokeLater(() ->
                JOptionPane.showMessageDialog(this, "Gagal memuat data pengeluaran: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE)
            );
            ex.printStackTrace();
        }
    }

