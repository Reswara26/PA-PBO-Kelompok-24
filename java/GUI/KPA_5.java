    // ‚úÖ Fungsi untuk load data progres dari database
    private void loadProgressData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Progres");
        model.addColumn("Nama Proyek");
        model.addColumn("Tanggal Update");
        model.addColumn("Persentase (%)");
        model.addColumn("Deskripsi");

        try (Connection conn = db_connection.getConnection()) {
            // üî• Query disesuaikan dengan tabel "progres"
            String query = """
                SELECT prg.id_progres, pr.nama_proyek, prg.tanggal_update, prg.persentase_selesai, prg.deskripsi
                FROM progres prg
                INNER JOIN proyek pr ON prg.Proyek_id_proyek = pr.id_proyek
                INNER JOIN pengguna p ON pr.id_proyek = p.Proyek_id_proyek
                WHERE p.username = ?;
            """;

            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, loggedInUsername);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_progres"),
                    rs.getString("nama_proyek"),
                    rs.getDate("tanggal_update"),
                    rs.getDouble("persentase_selesai"),
                    rs.getString("deskripsi")
                });
            }

            SwingUtilities.invokeLater(() -> jTable1.setModel(model));

            if (model.getRowCount() == 0) {
                SwingUtilities.invokeLater(() ->
                    JOptionPane.showMessageDialog(this, "Tidak ada data progres ditemukan.", "Informasi", JOptionPane.INFORMATION_MESSAGE)
                );
            }

        } catch (SQLException ex) {
            SwingUtilities.invokeLater(() ->
                JOptionPane.showMessageDialog(this, "Gagal memuat data progres: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE)
            );
            ex.printStackTrace();
        }
    }
    
