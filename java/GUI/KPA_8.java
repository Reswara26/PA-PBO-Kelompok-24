    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            // âœ… Tombol "Kirim" (UPDATE laporan)
        String idLaporan = jTextField4.getText().trim();
        String statusBaru = (String) jComboBox1.getSelectedItem();

        if (idLaporan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan ID laporan terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = db_connection.getConnection()) {
            // ðŸ”¹ Langkah 1: Ambil proyek admin login
            String getProyekSQL = "SELECT Proyek_id_proyek FROM pengguna WHERE username = ?";
            PreparedStatement psProyek = conn.prepareStatement(getProyekSQL);
            psProyek.setString(1, loggedInUsername);
            ResultSet rsProyek = psProyek.executeQuery();

            if (!rsProyek.next() || rsProyek.getString("Proyek_id_proyek") == null) {
                JOptionPane.showMessageDialog(this, "Admin belum memiliki proyek.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String proyekId = rsProyek.getString("Proyek_id_proyek");

            // ðŸ”¹ Langkah 2: Cek apakah laporan tersebut milik proyek admin login
            String cekSQL = "SELECT id_laporan FROM laporan WHERE id_laporan = ? AND Proyek_id_proyek = ?";
            PreparedStatement psCek = conn.prepareStatement(cekSQL);
            psCek.setString(1, idLaporan);
            psCek.setString(2, proyekId);
            ResultSet rsCek = psCek.executeQuery();

            if (!rsCek.next()) {
                JOptionPane.showMessageDialog(this, "Laporan tidak ditemukan atau bukan milik proyek Anda.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // ðŸ”¹ Langkah 3: Update status laporan
            String updateSQL = "UPDATE laporan SET status_tindak_lanjut = ? WHERE id_laporan = ? AND Proyek_id_proyek = ?";
            PreparedStatement stmt = conn.prepareStatement(updateSQL);
            stmt.setString(1, statusBaru);
            stmt.setString(2, idLaporan);
            stmt.setString(3, proyekId);

            int affected = stmt.executeUpdate();

            if (affected > 0) {
                JOptionPane.showMessageDialog(this, "Status laporan berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                KPA_7 lihatFrame = new KPA_7(loggedInUsername);
                lihatFrame.setLocationRelativeTo(null);
                lihatFrame.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui status laporan.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan Database: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

