    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // âœ… Tombol "Kirim" â†’ Update proyek milik kontraktor                                        
        String nama = jTextField1.getText().trim();
        String lokasi = jTextField4.getText().trim();
        String tanggalMulai = jFormattedTextField1.getText().trim();
        String tanggalSelesai = jFormattedTextField3.getText().trim();
        String anggaranStr = jFormattedTextField2.getText().trim();
        String status = (String) jComboBox1.getSelectedItem();

        if (nama.isEmpty() || lokasi.isEmpty() || tanggalMulai.isEmpty() || tanggalSelesai.isEmpty() 
                || anggaranStr.isEmpty() || status == null) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", 
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.time.format.DateTimeFormatter formatter = java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd");
        java.time.LocalDate mulaiDate, selesaiDate;

        try {
            mulaiDate = java.time.LocalDate.parse(tanggalMulai, formatter);
            selesaiDate = java.time.LocalDate.parse(tanggalSelesai, formatter);
        } catch (java.time.format.DateTimeParseException ex) {
            JOptionPane.showMessageDialog(this, 
                "Format tanggal harus yyyy-mm-dd dan tanggalnya valid!", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (selesaiDate.isBefore(mulaiDate)) {
            JOptionPane.showMessageDialog(this, 
                "Tanggal selesai tidak boleh lebih awal dari tanggal mulai!", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = db_connection.getConnection()) {
            // ðŸ”¹ Ambil ID proyek dari kontraktor login
            String getProjectId = "SELECT Proyek_id_proyek FROM pengguna WHERE username = ?";
            PreparedStatement ps1 = conn.prepareStatement(getProjectId);
            ps1.setString(1, loggedInUsername);
            ResultSet rs = ps1.executeQuery();

            if (!rs.next() || rs.getString("Proyek_id_proyek") == null) {
                JOptionPane.showMessageDialog(this, 
                    "Kontraktor belum memiliki proyek yang terhubung!", 
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String idProyek = rs.getString("Proyek_id_proyek");

            // ðŸ”¹ Update proyek berdasarkan ID itu
            String updateSQL = """
                UPDATE proyek
                SET nama_proyek = ?, lokasi = ?, tanggal_mulai = ?, tanggal_selesai = ?, 
                    anggaran = ?, status = ?
                WHERE id_proyek = ?;
            """;
            PreparedStatement ps2 = conn.prepareStatement(updateSQL);
            ps2.setString(1, nama);
            ps2.setString(2, lokasi);
            ps2.setDate(3, java.sql.Date.valueOf(mulaiDate));
            ps2.setDate(4, java.sql.Date.valueOf(selesaiDate));
            ps2.setBigDecimal(5, new java.math.BigDecimal(anggaranStr));
            ps2.setString(6, status);
            ps2.setString(7, idProyek);

            int rows = ps2.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, 
                    "Proyek berhasil diperbarui!", 
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);

                Menampilkan_Proyek tampilFrame = new Menampilkan_Proyek(loggedInUsername);
                tampilFrame.setLocationRelativeTo(null);
                tampilFrame.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Gagal memperbarui proyek. Pastikan data proyek tersedia!", 
                    "Gagal", JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, 
                "Kesalahan database: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Anggaran harus berupa angka!", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

