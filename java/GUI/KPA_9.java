    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            // âœ… Tombol "Kirim" (Hapus laporan berdasarkan ID)                                        
        String idLaporan = jTextField1.getText().trim();

        if (idLaporan.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Masukkan ID laporan yang ingin dihapus.", 
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = db_connection.getConnection()) {
            // ðŸ”¹ Langkah 1: Ambil proyek admin login
            String getProyekSQL = "SELECT Proyek_id_proyek FROM pengguna WHERE username = ?";
            PreparedStatement psProyek = conn.prepareStatement(getProyekSQL);
            psProyek.setString(1, loggedInUsername);
            ResultSet rsProyek = psProyek.executeQuery();

            if (!rsProyek.next() || rsProyek.getString("Proyek_id_proyek") == null) {
                JOptionPane.showMessageDialog(this, 
                    "Admin belum memiliki proyek.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String proyekId = rsProyek.getString("Proyek_id_proyek");

            // ðŸ”¹ Langkah 2: Ambil detail laporan
            String getLaporanSQL = """
                SELECT id_laporan, tanggal_laporan, jenis_kendala, status_tindak_lanjut
                FROM laporan 
                WHERE id_laporan = ? AND Proyek_id_proyek = ?;
            """;
            PreparedStatement psLaporan = conn.prepareStatement(getLaporanSQL);
            psLaporan.setString(1, idLaporan);
            psLaporan.setString(2, proyekId);
            ResultSet rsLaporan = psLaporan.executeQuery();

            if (!rsLaporan.next()) {
                JOptionPane.showMessageDialog(this, 
                    "Laporan tidak ditemukan atau bukan milik proyek Anda.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // ðŸ”¹ Simpan detail laporan untuk ditampilkan di popup
            String tanggal = rsLaporan.getString("tanggal_laporan");
            String jenis = rsLaporan.getString("jenis_kendala");
            String status = rsLaporan.getString("status_tindak_lanjut");

            // ðŸ”¹ Langkah 3: Konfirmasi penghapusan
            String pesanKonfirmasi = String.format("""
                Apakah Anda yakin ingin menghapus laporan berikut?

                ID Laporan     : %s
                Tanggal        : %s
                Jenis Kendala  : %s
                Status         : %s

                âš ï¸ Tindakan ini tidak dapat dibatalkan.
                """, idLaporan, tanggal, jenis, status);

            int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                pesanKonfirmasi,
                "Konfirmasi Penghapusan",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.ERROR_MESSAGE
            );

            if (konfirmasi != JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, 
                    "Penghapusan laporan dibatalkan.", 
                    "Dibatalkan", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            // ðŸ”¹ Langkah 4: Hapus laporan
            String deleteSQL = "DELETE FROM laporan WHERE id_laporan = ? AND Proyek_id_proyek = ?";
            PreparedStatement stmt = conn.prepareStatement(deleteSQL);
            stmt.setString(1, idLaporan);
            stmt.setString(2, proyekId);

            int rows = stmt.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, 
                    "âœ… Laporan berhasil dihapus!", 
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);
                KPA_7 lihatLaporanFrame = new KPA_7(loggedInUsername);
                lihatLaporanFrame.setLocationRelativeTo(null);
                lihatLaporanFrame.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Gagal menghapus laporan.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Kesalahan Database: " + ex.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

