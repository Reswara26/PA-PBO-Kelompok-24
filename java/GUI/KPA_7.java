    // âœ… Memuat data laporan khusus proyek milik admin login
    private void loadLaporanData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Laporan");
        model.addColumn("Tanggal Laporan");
        model.addColumn("Jenis Kendala");
        model.addColumn("Deskripsi");
        model.addColumn("Status Tindak Lanjut");
        model.addColumn("Proyek ID");

        try (Connection conn = db_connection.getConnection()) {
            // ðŸ”¹ Langkah 1: Ambil proyek milik admin login
            String proyekSQL = "SELECT Proyek_id_proyek FROM pengguna WHERE username = ?";
            PreparedStatement psProyek = conn.prepareStatement(proyekSQL);
            psProyek.setString(1, loggedInUsername);
            ResultSet rsProyek = psProyek.executeQuery();

            if (!rsProyek.next() || rsProyek.getString("Proyek_id_proyek") == null) {
                JOptionPane.showMessageDialog(this, "Admin belum memiliki proyek terdaftar.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                jTable1.setModel(model);
                return;
            }

            String proyekId = rsProyek.getString("Proyek_id_proyek");

            // ðŸ”¹ Langkah 2: Ambil laporan berdasarkan proyek admin login
            String laporanSQL = """
                SELECT id_laporan, tanggal_laporan, jenis_kendala, deskripsi, 
                       status_tindak_lanjut, Proyek_id_proyek
                FROM laporan 
                WHERE Proyek_id_proyek = ?
                ORDER BY tanggal_laporan DESC;
            """;

            PreparedStatement stmt = conn.prepareStatement(laporanSQL);
            stmt.setString(1, proyekId);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_laporan"),
                    rs.getDate("tanggal_laporan"),
                    rs.getString("jenis_kendala"),
                    rs.getString("deskripsi"),
                    rs.getString("status_tindak_lanjut"),
                    rs.getString("Proyek_id_proyek")
                });
            }

            jTable1.setModel(model);

            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Belum ada laporan untuk proyek Anda.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data laporan: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
